package com.message.entity;

public class SMS {
	
	private String To;
	private String Message;
	public String getTo() {
		return To;
	}
	public void setTo(String to) {
		To = to;
	}
	public String getMessage() {
		return Message;
	}
	public void setMessage(String message) {
		Message = message;
	}
	
	

}
